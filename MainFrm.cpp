void CMainFrame::OnApplicationLook(UINT id)
{
	CWaitCursor wait;

	theApp.m_nAppLook = id;
	HINSTANCE hInst = AfxGetResourceHandle();
	if(theApp.m_hInstEnglish) AfxSetResourceHandle(theApp.m_hInstEnglish);

	switch (theApp.m_nAppLook)
	{
	case ID_VIEW_APPLOOK_WIN_2000:
		CMFCVisualManager::SetDefaultManager(RUNTIME_CLASS(CMFCVisualManager));
		break;

	case ID_VIEW_APPLOOK_OFF_XP:
		CMFCVisualManager::SetDefaultManager(RUNTIME_CLASS(CMFCVisualManagerOfficeXP));
		break;

	case ID_VIEW_APPLOOK_WIN_XP:
		CMFCVisualManagerWindows::m_b3DTabsXPTheme = TRUE;
		CMFCVisualManager::SetDefaultManager(RUNTIME_CLASS(CMFCVisualManagerWindows));
		break;

	case ID_VIEW_APPLOOK_OFF_2003:
		CMFCVisualManager::SetDefaultManager(RUNTIME_CLASS(CMFCVisualManagerOffice2003));
		CDockingManager::SetDockingMode(DT_SMART);
		break;

	case ID_VIEW_APPLOOK_VS_2005:
		CMFCVisualManager::SetDefaultManager(RUNTIME_CLASS(CMFCVisualManagerVS2005));
		CDockingManager::SetDockingMode(DT_SMART);
		break;

	default:
		switch (theApp.m_nAppLook)
		{
		case ID_VIEW_APPLOOK_OFF_2007_BLUE:
			CMFCVisualManagerOffice2007::SetStyle(CMFCVisualManagerOffice2007::Office2007_LunaBlue);
			break;

		case ID_VIEW_APPLOOK_OFF_2007_BLACK:
			CMFCVisualManagerOffice2007::SetStyle(CMFCVisualManagerOffice2007::Office2007_ObsidianBlack);
			break;

		case ID_VIEW_APPLOOK_OFF_2007_SILVER:
			CMFCVisualManagerOffice2007::SetStyle(CMFCVisualManagerOffice2007::Office2007_Silver);
			break;

		case ID_VIEW_APPLOOK_OFF_2007_AQUA:
			CMFCVisualManagerOffice2007::SetStyle(CMFCVisualManagerOffice2007::Office2007_Aqua);
			break;
		}

		CMFCVisualManager::SetDefaultManager(RUNTIME_CLASS(CMFCVisualManagerOffice2007));
		CDockingManager::SetDockingMode(DT_SMART);
	}

	AfxSetResourceHandle(hInst);
	RedrawWindow(NULL, NULL, RDW_ALLCHILDREN | RDW_INVALIDATE | RDW_UPDATENOW | RDW_FRAME | RDW_ERASE);

	theApp.WriteInt(_T("ApplicationLook"), theApp.m_nAppLook);
}

void CMainFrame::OnUpdateApplicationLook(CCmdUI* pCmdUI)
{
	pCmdUI->SetRadio(theApp.m_nAppLook == pCmdUI->m_nID);
}

BOOL CMainFrame::LoadFrame(UINT nIDResource, DWORD dwDefaultStyle, CWnd* pParentWnd, CCreateContext* pContext) 
{
	// base class does the real work

	if (!CMDIFrameWndEx::LoadFrame(nIDResource, dwDefaultStyle, pParentWnd, pContext))
	{
		return FALSE;
	}


	// enable customization button for all user toolbars
	BOOL bNameValid;
	CString strCustomize;
	bNameValid = strCustomize.LoadString(IDS_TOOLBAR_CUSTOMIZE);
	ASSERT(bNameValid);

	for (int i = 0; i < iMaxUserToolbars; i ++)
	{
		CMFCToolBar* pUserToolbar = GetUserToolBarByIndex(i);
		if (pUserToolbar != NULL)
		{
			pUserToolbar->EnableCustomizeButton(TRUE, ID_VIEW_CUSTOMIZE, strCustomize);
		}
	}

	return TRUE;
}

// Change the language: [IN]nLanguage: The new language.
void CMainFrame::SetLanguage_(int nLanguage, BOOL bLoadAccelTable /*= TRUE*/)
{
	HINSTANCE hInst = NULL;

	// Store the Default resource instance
	if(!theApp.m_hInstEnglish)
		theApp.m_hInstEnglish = AfxGetInstanceHandle();
	// Load the relevant resource DLL
	switch(nLanguage)
	{
	case ID_LANGUAGE_FARSI:
		// Only call LoadLibrary once, because 
		// using LoadLibrary() and FreeLibrary()
		// seemed to cause problems, i.e repeatedly 
		// removing the library after use, in VC++ 6.0 & NT 4.0
		if(!theApp.m_hInstFarsi)
			theApp.m_hInstFarsi = LoadLibrary(_T("LangFa.dll"));
		theApp.m_bIsRTL = TRUE;
		hInst = theApp.m_hInstFarsi;
		break;
	case ID_LANGUAGE_ARABIC:
		if(!theApp.m_hInstArabic)
			theApp.m_hInstArabic = LoadLibrary(_T("LangAr.dll"));
		theApp.m_bIsRTL = TRUE;
		hInst = theApp.m_hInstArabic;
		break;
	default:
		theApp.m_bIsRTL = FALSE;
		hInst = theApp.m_hInstEnglish;
	}

	// If we have successfully loaded a resource DLL, use it, otherwise
	// load the resources from the EXE
	if(hInst)
	{
		theApp.m_nLanguage = nLanguage;
		AfxSetResourceHandle(hInst);
	}
	else
	{
		TRACE("ERROR: Language resource data not loaded!");
		theApp.m_bIsRTL = FALSE;
		theApp.m_nLanguage = ID_LANGUAGE_ENGLISH;
		AfxSetResourceHandle(theApp.m_hInstEnglish);
	}
	
	//OnChangeLayout();
	//RemoveBars();
	//CreateBars();
	//SetTooltipRTL(theApp.m_bIsRTL);

	if(bLoadAccelTable)
	{
		m_hAccelTable = NULL;
		if(!LoadAccelTable(MAKEINTRESOURCE(IDR_MAINFRAME)))
			TRACE("ERROR in loading AccelTable!");
		//ReportError(_T("LoadAccelTable(MAKEINTRESOURCE(IDR_MAINFRAME)"), GetLastError());
		ChangeMyTexts();
	}
}
void CMainFrame::SetLanguage(UINT id, BOOL bSaveInReg /*= TRUE*/, BOOL bForceSet /*= FALSE*/, BOOL bLoadAccelTable /*= TRUE*/)
{
	if(id == theApp.m_nLanguage && !bForceSet) return;
	CWaitCursor wait;
	SetLanguage_(id, bLoadAccelTable);
	RedrawWindow(NULL, NULL, RDW_ALLCHILDREN | RDW_INVALIDATE | RDW_UPDATENOW | RDW_FRAME | RDW_ERASE);
	if(bSaveInReg) theApp.WriteInt(_T("ApplicationLanguage"), theApp.m_nLanguage);
}

void CMainFrame::OnLanguage(UINT id)
{
	SetLanguage(id);			
}
